# Example of IF / ELSE condition in Python:
# if condition:
#   do this
# else:
#   do that

# Functioning example:
water_level = 50
if water_level > 80:
    print("Drain water.")
else:
    print("Continue.")

# Another conditional
print("Welcome to my fair.")
your_height = int(input("Enter your height in cm. "))

if your_height >= 120:
    print("You can enter my fair.")
else:
    print("You cannot enter my fair, grow taller.")
